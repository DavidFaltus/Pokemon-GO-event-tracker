export const getPokemonHubRating = (name: string): string => {
  const clean = name.toLowerCase()
    .replace('shadow ', '')
    .replace('apex ', '')
    .replace('mega ', '')
    .replace('primal ', '')
    .replace(/\s*\(.*?\)\s*/g, '')
    .trim();
  return LOCAL_HUB_RATING_DB[clean] || '';
};

